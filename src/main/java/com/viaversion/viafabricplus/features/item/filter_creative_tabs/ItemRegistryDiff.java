   public static boolean keepItem(Item item) {
      if (ProtocolTranslator.getTargetVersion().equals(LegacyProtocolVersion.c0_30cpe)) {
         ClientPlayNetworkHandler handler = MinecraftClient.getInstance().getNetworkHandler();
         if (handler == null) {
            return true;
         }

         ExtensionProtocolMetadataStorage extensionProtocol = (ExtensionProtocolMetadataStorage)((IClientConnection)handler.getConnection())
            .viaFabricPlus$getUserConnection()
            .get(ExtensionProtocolMetadataStorage.class);
         if (extensionProtocol == null) {
            return false;
         }

         if (extensionProtocol.hasServerExtension(ClassicProtocolExtension.CUSTOM_BLOCKS, new int[]{1}) && EXTENDED_CLASSIC_ITEMS.contains(item)) {
            return true;
         }
      }

      return contains(item, ProtocolTranslator.getTargetVersion());
   }

   public static boolean contains(Item item, ProtocolVersion version) {
      return !ITEM_DIFF.containsKey(item) || ITEM_DIFF.get(item).contains(version);
   }

